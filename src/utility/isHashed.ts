/* -----------------------------------
 *
 * Regex
 *
 * -------------------------------- */

const regex = /\.([a-zA-Z0-9]){8}/;

/* -----------------------------------
 *
 * Hashed
 *
 * -------------------------------- */

function isHashed(name: string) {
  return name.match(regex);
}

/* -----------------------------------
 *
 * Export
 *
 * -------------------------------- */

export { isHashed };
